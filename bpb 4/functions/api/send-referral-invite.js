// ═══════════════════════════════════════════════════════════════════════════
// /api/send-referral-invite  —  Phase 4.0c
//
// Homeowner-callable endpoint. The signed-in homeowner refers a friend by
// providing their name + email + optional phone + optional personal note.
// This function:
//
//   1. Verifies the caller's JWT corresponds to a homeowner clients row.
//   2. Validates the input (name, email, optional phone, optional note).
//   3. Blocks duplicate active invites to the same email from the same referrer.
//   4. Inserts a referrals row (status='sent'). The scheduling_token is
//      auto-generated by the column's DEFAULT. The personal_note is stored
//      so resend-referral-invite can re-use it on reminder sends.
//   5. Sends the invite email via Resend, written in the referrer's voice
//      ("from Tim McMullen") with a $500 incentive callout and a CTA that
//      links to /refer/?t={scheduling_token} on this domain. If a personal
//      note was provided, it's rendered as a styled blockquote in the email
//      between the opener paragraph and the $500 callout.
// ═══════════════════════════════════════════════════════════════════════════

// Canonical public URL used in all outbound email links. Hardcoded rather
// than derived from request.url so emails always point at the branded
// portal-baysidepavers.com domain regardless of which host the API was
// reached through (.pages.dev preview deployments, custom domain, etc.)
const PUBLIC_BASE_URL = 'https://portal-baysidepavers.com';

// Max length for the homeowner's personal note. 1000 chars covers a generous
// paragraph — the modal textarea is 3 rows so realistic notes are 100-300 chars.
const PERSONAL_NOTE_MAX = 1000;

export async function onRequestPost({ request, env }) {
  const json = (status, body) => new Response(JSON.stringify(body), {
    status,
    headers: { 'Content-Type': 'application/json' },
  });

  try {
    const SUPABASE_URL    = env.SUPABASE_URL;
    const SERVICE_ROLE    = env.SUPABASE_SERVICE_ROLE_KEY;
    const RESEND_API_KEY  = env.RESEND_API_KEY;
    const RESEND_FROM     = env.RESEND_FROM || 'Tim McMullen <tim@mcmullen.properties>';

    if (!SUPABASE_URL || !SERVICE_ROLE) {
      return json(500, { error: 'Server not configured' });
    }

    const auth = request.headers.get('authorization') || '';
    const token = auth.startsWith('Bearer ') ? auth.slice(7).trim() : '';
    if (!token) return json(401, { error: 'Missing auth token' });

    const userResp = await fetch(SUPABASE_URL + '/auth/v1/user', {
      headers: { 'Authorization': 'Bearer ' + token, 'apikey': SERVICE_ROLE },
    });
    if (!userResp.ok) return json(401, { error: 'Invalid auth token' });
    const callerUser = await userResp.json();
    if (!callerUser || !callerUser.id) {
      return json(401, { error: 'Invalid auth token (no user)' });
    }

    const referrerResp = await fetch(
      SUPABASE_URL + '/rest/v1/clients?user_id=eq.' + encodeURIComponent(callerUser.id) +
      '&select=id,name,email',
      { headers: { 'apikey': SERVICE_ROLE, 'Authorization': 'Bearer ' + SERVICE_ROLE } }
    );
    if (!referrerResp.ok) return json(403, { error: 'Could not look up referrer account' });
    const referrerRows = await referrerResp.json();
    const referrer = Array.isArray(referrerRows) && referrerRows[0];
    if (!referrer) return json(403, { error: 'Only homeowner accounts can send referrals' });

    const body = await request.json().catch(() => null);
    if (!body || typeof body !== 'object') return json(400, { error: 'Invalid JSON body' });

    const refEmail = String(body.referred_email || '').trim().toLowerCase();
    const refName  = String(body.referred_name  || '').trim();
    const refPhoneIn = String(body.referred_phone || '').trim();
    const refPhone = refPhoneIn || null;
    const personalNoteIn = String(body.personal_note || '').trim();
    const personalNote = personalNoteIn || null;

    if (!refEmail || !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(refEmail)) return json(400, { error: 'Invalid email address for your friend' });
    if (!refName) return json(400, { error: "Please add your friend's name" });
    if (refName.length > 120) return json(400, { error: 'Name too long (max 120 chars)' });
    if (refEmail === referrer.email.toLowerCase()) return json(400, { error: "You can't refer yourself!" });
    if (personalNoteIn.length > PERSONAL_NOTE_MAX) return json(400, { error: 'Personal note too long (max ' + PERSONAL_NOTE_MAX + ' chars)' });

    const dupResp = await fetch(
      SUPABASE_URL + '/rest/v1/referrals' +
      '?referrer_client_id=eq.' + encodeURIComponent(referrer.id) +
      '&referred_email=eq.' + encodeURIComponent(refEmail) +
      '&status=in.(sent,scheduled,appointment_completed,credit_awarded)' +
      '&select=id,status',
      { headers: { 'apikey': SERVICE_ROLE, 'Authorization': 'Bearer ' + SERVICE_ROLE } }
    );
    if (dupResp.ok) {
      const dups = await dupResp.json();
      if (Array.isArray(dups) && dups.length > 0) {
        return json(409, { error: "You've already sent an invite to that email — they should still have it in their inbox." });
      }
    }

    const insertResp = await fetch(SUPABASE_URL + '/rest/v1/referrals', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'apikey': SERVICE_ROLE,
        'Authorization': 'Bearer ' + SERVICE_ROLE,
        'Prefer': 'return=representation',
      },
      body: JSON.stringify({
        referrer_client_id: referrer.id,
        referred_email: refEmail,
        referred_name:  refName,
        referred_phone: refPhone,
        personal_note:  personalNote,
        status: 'sent',
      }),
    });

    if (!insertResp.ok) {
      const errText = await insertResp.text();
      return json(502, { error: 'Could not create referral: ' + errText.slice(0, 240) });
    }
    const insertedRows = await insertResp.json();
    const referral = Array.isArray(insertedRows) ? insertedRows[0] : insertedRows;

    const origin = PUBLIC_BASE_URL;
    const landingUrl = origin + '/refer/?t=' + encodeURIComponent(referral.scheduling_token);
    const referrerFirstName = (referrer.name || '').split(/[\s,&]+/)[0] || 'a Paver Portal customer';
    const refeeFirstName    = (refName || '').split(/[\s,&]+/)[0] || 'there';

    let emailSent = false;
    let emailError = null;

    if (RESEND_API_KEY) {
      try {
        const emailResp = await fetch('https://api.resend.com/emails', {
          method: 'POST',
          headers: { 'Authorization': 'Bearer ' + RESEND_API_KEY, 'Content-Type':  'application/json' },
          body: JSON.stringify({
            from:    RESEND_FROM,
            to:      [refEmail],
            reply_to: referrer.email,
            subject: referrerFirstName + " thinks you'll love what Paver Portal is doing for their backyard",
            html:    buildInviteHtml({ referrerName: referrer.name, refeeFirstName, landingUrl, personalNote }),
            text:    buildInviteText({ referrerName: referrer.name, refeeFirstName, landingUrl, personalNote }),
          }),
        });
        if (emailResp.ok) {
          emailSent = true;
        } else {
          emailError = 'Resend ' + emailResp.status + ': ' + (await emailResp.text()).slice(0, 240);
        }
      } catch (err) {
        emailError = 'Resend fetch failed: ' + ((err && err.message) || 'unknown');
      }
    } else {
      emailError = 'RESEND_API_KEY not configured';
    }

    return json(200, {
      ok: true,
      referral_id:       referral.id,
      scheduling_token:  referral.scheduling_token,
      landing_url:       landingUrl,
      email_sent:        emailSent,
      email_error:       emailError,
    });

  } catch (err) {
    return json(500, { error: (err && err.message) || 'Unexpected server error' });
  }
}

export async function onRequestOptions() {
  return new Response(null, {
    status: 204,
    headers: {
      'Access-Control-Allow-Origin':  '*',
      'Access-Control-Allow-Methods': 'POST, OPTIONS',
      'Access-Control-Allow-Headers': 'Content-Type, Authorization',
      'Access-Control-Max-Age':       '86400',
    },
  });
}

